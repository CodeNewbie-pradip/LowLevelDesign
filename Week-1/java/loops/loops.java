package loops;

public class loops {
    public static void main(String[] args) {
        System.out.println("It is while loops class");
        int i=0;
        while(i<5)
        {
            System.out.println("inside loops");
            i++;
        }

        for(int j=1; j<=5; j++)
        {
            System.out.println(j);
        }
    }
}
